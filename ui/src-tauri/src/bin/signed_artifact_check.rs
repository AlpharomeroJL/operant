// A real, runnable verification of the updater wiring: the full check ->
// staged download -> Ed25519 signature verification round trip against a
// local fixture HTTP server, then a tampered-manifest rejection check. Uses
// the real `tauri_plugin_updater` crate end to end, nothing reimplemented or
// mocked at the network or signature-verification layer.
//
// Run with:
//   cargo run --bin signed_artifact_check --features fixture-check
//
// Why this is a `[[bin]]` and not a `#[test]` under tests/: in this
// environment, confirmed by direct experiment, any `cargo test`-built binary
// (an integration test under tests/, or an `[[example]]`, i.e. anything
// landing under target/<profile>/deps/ or target/<profile>/examples/) that
// calls tauri::Builder::build() crashes at process load with
// STATUS_ENTRYPOINT_NOT_FOUND (0xC0000139). That reproduces with zero
// updater-specific code: a bare
// `tauri::Builder::default().build(tauri::generate_context!())`, no plugins
// registered at all, already triggers it there. The identical call as a
// plain `[[bin]]` target (landing directly in target/<profile>/, the same
// place the real operant-shell.exe lands and runs successfully) does not
// crash. So this is a pre-existing environment/toolchain characteristic of
// this sandboxed machine, not a defect in this packet's code; see this
// lane's return notes for the full isolation trail (a bisection across
// MockRuntime vs the real Wry runtime, the "test" feature on and off, and
// harness = true/false, all pointing at the same target-location variable).
//
// What this does and does not prove, stated plainly: it proves the real
// signature-verification code path accepts a correctly-signed artifact and
// rejects a tampered one, and that verified bytes reach the real
// `Update::install` call. It does not, and cannot safely, prove a real NSIS
// installer actually swaps files on disk and relaunches, or that a real
// Windows toast notification renders on screen; both need either a live
// update server or an interactive desktop session. See
// docs/KNOWN_ISSUES.md and src/updater.rs's module doc for the same caveat
// stated where a reader would look for it later.
//
// Fixture material under ../../tests/fixtures/updater/ is committed,
// pre-generated by tests/fixtures/updater/generate.mjs using a throwaway
// test-only Ed25519 keypair that is never written to disk and is never the
// real release signing key documented in release/KEYS.md.

use tauri_plugin_updater::UpdaterExt;

const ARTIFACT: &[u8] = include_bytes!("../../tests/fixtures/updater/artifact.bin");
const MANIFEST_TEMPLATE: &str = include_str!("../../tests/fixtures/updater/latest.json");
const TAMPERED_SIGNATURE: &str = include_str!("../../tests/fixtures/updater/tampered-signature.txt");
const PUBKEY: &str = include_str!("../../tests/fixtures/updater/pubkey.tauri-config-value.txt");

struct FixtureServer {
    addr: std::net::SocketAddr,
}

impl FixtureServer {
    /// Spawns a background thread that serves exactly two requests (the
    /// manifest, then the artifact) and returns. Deliberately not joined
    /// anywhere: an earlier version of this bounded the server at more
    /// requests than a single check-and-download round trip actually makes,
    /// so `incoming_requests()` blocked forever on a request that was never
    /// coming, and joining that handle on drop hung the whole process. Two
    /// short-lived, independent fixture servers per run, never joined, and
    /// left to be reclaimed when the process exits, sidesteps that class of
    /// bug entirely instead of just picking a request count and hoping nothing
    /// ever needs a retry.
    fn start(manifest_body: String) -> Self {
        let server = tiny_http::Server::http("127.0.0.1:0").expect("failed to bind the fixture server");
        let addr = server
            .server_addr()
            .to_ip()
            .expect("fixture server should be bound to an IP address");
        let manifest_body = manifest_body.replace("{{BASE_URL}}", &format!("http://{addr}"));

        std::thread::spawn(move || {
            for request in server.incoming_requests().take(2) {
                let url = request.url().to_string();
                let outcome = if url == "/latest.json" {
                    let header = tiny_http::Header::from_bytes(&b"Content-Type"[..], &b"application/json"[..]).unwrap();
                    request.respond(tiny_http::Response::from_string(manifest_body.clone()).with_header(header))
                } else if url == "/artifact.bin" {
                    let header =
                        tiny_http::Header::from_bytes(&b"Content-Type"[..], &b"application/octet-stream"[..]).unwrap();
                    request.respond(tiny_http::Response::from_data(ARTIFACT.to_vec()).with_header(header))
                } else {
                    request.respond(tiny_http::Response::from_string("not found").with_status_code(404))
                };
                let _ = outcome;
            }
        });

        Self { addr }
    }

    fn manifest_url(&self) -> url::Url {
        format!("http://{}/latest.json", self.addr)
            .parse()
            .expect("constructed URL should always be valid")
    }
}

fn build_app() -> tauri::App<tauri::Wry> {
    // Zero configured windows: this check only needs an AppHandle to reach
    // UpdaterExt, and clearing the window list keeps this a headless,
    // non-visual check instead of popping a real window open.
    let mut context = tauri::generate_context!();
    context.config_mut().app.windows.clear();
    tauri::Builder::default()
        .plugin(tauri_plugin_updater::Builder::new().build())
        .build(context)
        .expect("failed to build the app")
}

fn manifest_signature(manifest_json: &str) -> String {
    let value: serde_json::Value = serde_json::from_str(manifest_json).expect("fixture manifest must be valid JSON");
    value["signature"]
        .as_str()
        .expect("fixture manifest must have a string signature field")
        .to_string()
}

async fn check_download_and_verify_against_fixture_server() {
    println!("--- check_download_and_verify_against_fixture_server ---");
    let server = FixtureServer::start(MANIFEST_TEMPLATE.to_string());
    let app = build_app();

    let updater = app
        .handle()
        .updater_builder()
        .endpoints(vec![server.manifest_url()])
        .expect("fixture endpoint should be a valid (http, allowed in debug builds) URL")
        .pubkey(PUBKEY.trim())
        .build()
        .expect("failed to build the updater against the fixture config");

    let update = updater
        .check()
        .await
        .expect("check() should succeed against a well-formed, correctly-signed fixture manifest")
        .expect("the fixture manifest's version (9.9.9) is newer than the app's, so an update should be reported");
    assert_eq!(update.version, "9.9.9");
    println!("check(): OK, reported version {}", update.version);

    let bytes = update
        .download(|_chunk_len, _total_len| {}, || {})
        .await
        .expect("download() verifies the Ed25519 signature internally; a correctly-signed artifact must pass");
    assert_eq!(
        bytes, ARTIFACT,
        "the bytes handed back after verification should be exactly the signed fixture artifact"
    );
    println!("download(): OK, {} bytes downloaded and Ed25519-verified against release/KEYS.md's wire format", bytes.len());

    // The swap step. `install` here is the real, unmodified plugin method,
    // not a stand-in, handed the exact bytes that just passed verification
    // above. The fixture artifact is deliberately not a real Windows
    // executable, so `install` fails fast at its own "is this actually an
    // installer" sniff (checked before anything is ever shelled out to)
    // rather than launching a process, proving the real call is reachable
    // and wired to genuinely-verified bytes without needing to run a real
    // installer to prove it.
    let install_result = update.install(bytes);
    match &install_result {
        Err(tauri_plugin_updater::Error::InvalidUpdaterFormat) => {
            println!("install(): OK, reached the real swap call; rejected as InvalidUpdaterFormat (fixture bytes are not a real installer, as expected)");
        }
        other => panic!("expected install() to reach its real format-sniff step and reject the non-installer fixture bytes with InvalidUpdaterFormat, got: {other:?}"),
    }
    println!("PASS: check_download_and_verify_against_fixture_server\n");
}

async fn tampered_manifest_signature_is_rejected() {
    println!("--- tampered_manifest_signature_is_rejected ---");
    let original_signature = manifest_signature(MANIFEST_TEMPLATE);
    let tampered_manifest = MANIFEST_TEMPLATE.replacen(&original_signature, TAMPERED_SIGNATURE.trim(), 1);
    assert_ne!(
        tampered_manifest, MANIFEST_TEMPLATE,
        "sanity check: the tampering substitution must actually change the manifest text"
    );

    let server = FixtureServer::start(tampered_manifest);
    let app = build_app();

    let updater = app
        .handle()
        .updater_builder()
        .endpoints(vec![server.manifest_url()])
        .expect("fixture endpoint should be a valid URL")
        .pubkey(PUBKEY.trim())
        .build()
        .expect("failed to build the updater against the fixture config");

    // check() only parses the manifest JSON and compares version numbers; it
    // does not touch the signature at all, so a tampered signature field
    // alone does not fail it. The rejection has to happen at download(),
    // which is exactly what makes it meaningful: a manifest served over a
    // compromised or MITM'd channel is still cryptographically checked
    // before any bytes are trusted, not just structurally validated.
    let update = updater
        .check()
        .await
        .expect("check() does not verify signatures, so a tampered signature field alone must not fail it")
        .expect("the fixture manifest's version is still newer, so an update should still be reported");
    println!("check(): OK, reported version {} (signature not checked yet)", update.version);

    let result = update.download(|_chunk_len, _total_len| {}, || {}).await;
    match &result {
        Err(err @ (tauri_plugin_updater::Error::Minisign(_) | tauri_plugin_updater::Error::Base64(_))) => {
            println!("download(): OK, rejected the tampered manifest's signature with: {err}");
        }
        Ok(_) => panic!("a manifest with a tampered signature field must be rejected at download/verify time, not accepted"),
        Err(other) => panic!("expected a signature-verification error (Minisign or Base64), got: {other:?}"),
    }
    println!("PASS: tampered_manifest_signature_is_rejected\n");
}

// Plain panics (assert!/expect/panic!) are enough signal here: Rust prints
// the panic message and exits with a non-zero code (101) on its own, and a
// clean run falls through to "ALL CHECKS PASSED" with exit code 0. No need
// for a manual catch_unwind/ExitCode dance on top of that.
#[tokio::main]
async fn main() {
    check_download_and_verify_against_fixture_server().await;
    tampered_manifest_signature_is_rejected().await;
    println!("ALL CHECKS PASSED");
}
